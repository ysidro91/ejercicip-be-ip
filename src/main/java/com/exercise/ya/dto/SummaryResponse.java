package com.exercise.ya.dto;

public class SummaryResponse {

	private String greaterDistance;
	private String shorterDistance;
	private String averageTotal;

	public String getGreaterDistance() {
		return greaterDistance;
	}

	public void setGreaterDistance(String greaterDistance) {
		this.greaterDistance = greaterDistance;
	}

	public String getShorterDistance() {
		return shorterDistance;
	}

	public void setShorterDistance(String shorterDistance) {
		this.shorterDistance = shorterDistance;
	}

	public String getAverageTotal() {
		return averageTotal;
	}

	public void setAverageTotal(String averageTotal) {
		this.averageTotal = averageTotal;
	}

}
